            <div id="faq">
                <h4>Q: What is Vehicles in Nepal(VIN)?</h4>
                <p>VIN is a open-data project(website) which is free to use, reuse, and redistribute. This project is available on <b>GitHub</b>.</p>
                <br />
                <h4>Q: What is Vehicles in Nepal(VIN) for?</h4>
                <p>This website contains information and statistics on roads, vehicles and accidents in Nepal. The name can be misleading though.</p>
                <br />
                <h4>Q: What type of information does VIN provide?</h4>
                <p>This website provides specific information on the status of vehicles, accidents and road networks based on official data collected from various sources.</p>
                <br />
                <h4>Q: Who is VIN for?</h4>
                <p>It is for policy makers, citizens, engineers and everyone interested.</p>
                <br />
                <h4>Q: Where do the data come from?</h4>
                <p>The data are collected from various sources which include: Department of Road, Department of Transport Management, Nepal Road Safety Action Plan (2011-2020) and UNESCAP - UN Economic and Social Commission for Asia and the Pacific. The specific data sources can be found on the right section of this page.</p>
                <br />
                <h4>Q: Is any license used for the data?</h4>
                <p>The data are open and made available under the <a href="http://www.opendatacommons.org/licenses/pddl/1.0/">Public Domain Dedication and License v1.0</a></p>
                <br />
                <h4>Q: Are these data valid?</h4>
                <p>Yes, the data are made available through official sources. The data are being used as it is without any alteration.</p>
                <br />
                <h4>Q: Are these data available for download?</h4>
                <p>Yes, they are available in open data formats for download. You can download these data from the <a href="<?= base_url() ?>resources">resources</a> page. We provide three type of open data formats which are: CSV, JSON, XML.</p>
                <br />
                <h4>Q: Where do I contact if there is any issues with the data?</h4>
                <p>Anyone can contact us through <a href="<?= base_url() ?>contact">contact</a> page of this website. Any queries and suggestions are always welcome.</p>
